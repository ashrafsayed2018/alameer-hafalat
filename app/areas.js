export function getAreaBySlug(areaSlug) {
  const decoded = decodeURIComponent(areaSlug).normalize('NFC').trim()
  for (const gov of governorates) {
    const area = gov.areas.find((a) => a.slug === decoded || a.ar === decoded)
    if (area) return { area, governorate: gov }
  }
  return null
}

export function getAllAreaSlugs() {
  return governorates.flatMap((gov) => gov.areas.map((a) => a.slug))
}
